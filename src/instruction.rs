#[derive(Debug)]
pub enum Instruction {
    Push(i32),
    Dup,
    Copy(i32),
    Swap,
    Discard,
    Slide(i32),
    Add,
    Sub,
    Mul,
    Div,
    Mod,
    MemWrite,
    MemRead,
    Label(String),
    Call(String),
    Jump(String),
    JumpZero(String),
    JumpNegs(String),
    Return,
    Exit,
    CharOut,
    NumOut,
    CharIn,
    NumIn,
}
